static void BM_Bareiss2000(ankerl::nanobench::Bench &bench) {
  const size_t N = 20;
  IntMatrix A(Row{N}, Col{N});
  A(0, 0) = 2;
  for (size_t i = 1; i < N; ++i) {
    A(i - 1, i) = -1;
    A(i, i) = 2;
    A(i, i - 1) = -1;
  }
  for (size_t j = 0; j < N; j += 8) {
    // A(j,:)
    for (size_t i = 0; i < N; ++i) A(j, i) = 0;
    for (size_t i = 0; i < N; i += 7) {
      int64_t s = (i & 1) ? 1 : -1;
      for (size_t k = 0; k < N; ++k) A(j, k) += s * A(i, k);
    }
  }
  // std::cout << A << std::endl;

  // fourth row is 0
  llvm::SmallVector<size_t, 16> pivots;
  pivots.reserve(N);
  IntMatrix B;
  bench.run("Bareiss2000", [&] {
    pivots.clear();
    B = A;
    NormalForm::bareiss(B, pivots);
  });
}

